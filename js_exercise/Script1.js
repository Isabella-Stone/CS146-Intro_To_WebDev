function pep111() {
        var pep111lec = document.getElementById('pep111lec')
        var pep111x = document.getElementsByClassName('PEP111')
        var pep111color = document.getElementById('PEP111color').value

        if (pep111lec.innerHTML == '') {
            for (i=0; i<pep111x.length; i++) {
                pep111x[i].innerHTML = 'PEP111'
                pep111x[i].style.backgroundColor = pep111color
            }
        } 
        else {
            for (i=0; i<pep111x.length; i++) {
                pep111x[i].innerHTML = ''
                pep111x[i].style.backgroundColor = "white"
            }
        }
    }

